  var count = 0
  let gridWidth = grid[0].len
  let gridHeight = grid.len

  let needle1 = "MAS"
  let needle2 = "SAM"

  for y in 0 ..< gridHeight:
    for x in 0 ..< gridWidth:
      if x >= 1 and y >= 1 and x <= gridWidth - 2 and y <= gridHeight - 2:
        let diagonalDownRight = grid[y-1][x-1] & grid[y][x] & grid[y+1][x+1]
        if diagonalDownRight == "MAS" or diagonalDownRight == "SAM":
          let diagonalDownLeft = grid[y-1][x+1] & grid[y][x] & grid[y+1][x-1]
          if diagonalDownLeft == "MAS" or diagonalDownLeft == "SAM":
            count += 1

  return count

