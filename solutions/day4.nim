func getInput(): string

proc getGrid(): seq[string] =
  return getInput().splitlines().filter do (line: string) -> bool: not line.isEmptyOrWhitespace()

proc countStr(s: string, f: string): int =
  var count = 0
  var idx = s.find(f, 0)
  while (idx >= 0):
    count += 1
    idx = s.find(f, idx + 1)
  return count


proc rotateGrid(grid: seq[string]): seq[string] =
  var newGrid: seq[string] = @[]
  let gridWidth = grid[0].len
  let gridHeight = grid.len
  for x in 0 ..< gridWidth:
    var newRow = ""
    for y in countdown(gridHeight - 1, 0):
      # echo fmt"x: {x}, y:{y}"
      newRow = newRow & grid[y][x]
    newGrid.add(newRow)
  return newGrid


proc countXmasLeftToRight(grid: seq[string]): int =
  return sum(grid.map do (s: string) -> int: countStr(s, "XMAS"))


proc printGrid(grid: seq[string]) =
  for line in grid:
    echo line


proc findAllXmas0(grid: seq[string]): int =
  # Find all left to right
  printGrid(grid)
  let leftToRight = countXmasLeftToRight(grid)
  echo fmt"found: {leftToRight}"
  let gridRotate1 = rotateGrid(grid)
  printGrid(gridRotate1)
  let bottomToTop = countXmasLeftToRight(gridRotate1)
  echo fmt"found: {bottomToTop}"
  let gridRotate2 = rotateGrid(gridRotate1)
  printGrid(gridRotate2)
  let rightToLeft = countXmasLeftToRight(gridRotate2)
  echo fmt"found: {rightToLeft}"
  let gridRotate3 = rotateGrid(gridRotate2)
  printGrid(gridRotate3)
  let topToBottom = countXmasLeftToRight(gridRotate3)
  echo fmt"found: {topToBottom}"

  return leftToRight + bottomToTop + rightToLeft + topToBottom


proc findAllXmas1(grid: seq[string]): int =
  printGrid(grid)

  var count = 0
  let gridWidth = grid[0].len
  let gridHeight = grid.len

  let needle = "XMAS"

  for y in 0 ..< gridHeight:
    for x in 0 ..< gridWidth:
      if x <= gridWidth - needle.len:
        let leftToRight = grid[y][x .. x+3]
        if leftToRight == needle:
          count += 1

      if x >= needle.len - 1:
        let rightToLeft = grid[y][x] & grid[y][x-1] & grid[y][x-2] & grid[y][x-3]
        if rightToLeft == needle:
          count += 1

      if y <= gridHeight - needle.len:
        let topToBottom = grid[y][x] & grid[y+1][x] & grid[y+2][x] & grid[y+3][x]
        if topToBottom == needle:
          count += 1

      if y >= needle.len - 1:
        let bottomToTop = grid[y][x] & grid[y-1][x] & grid[y-2][x] & grid[y-3][x]
        if bottomToTop == needle:
          count += 1

      if x <= gridWidth - needle.len and y <= gridHeight - needle.len:
        let diagonalDownRight = grid[y][x] & grid[y+1][x+1] & grid[y+2][x+2] & grid[y+3][x+3]
        if diagonalDownRight == needle:
          # echo fmt"x: {x}, y: {y}"
          count += 1

      if x >= needle.len - 1 and y <= gridHeight - needle.len:
        let diagonalDownLeft = grid[y][x] & grid[y+1][x-1] & grid[y+2][x-2] & grid[y+3][x-3]
        if diagonalDownLeft == needle:
          # echo fmt"x: {x}, y: {y}"
          count += 1

      if x <= gridWidth - needle.len and y >= needle.len - 1:
        let diagonalUpRight = grid[y][x] & grid[y-1][x+1] & grid[y-2][x+2] & grid[y-3][x+3]
        if diagonalUpRight == needle:
          # echo fmt"x: {x}, y: {y}"
          count += 1

      if x >= needle.len - 1 and y >= needle.len - 1:
        let diagonalUpLeft = grid[y][x] & grid[y-1][x-1] & grid[y-2][x-2] & grid[y-3][x-3]
        if diagonalUpLeft == needle:
          # echo fmt"x: {x}, y: {y}"
          count += 1

  return count

proc findAllXmas2(grid: seq[string]): int =
